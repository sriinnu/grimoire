export async function loadPhosphorIcon(name: string): Promise<ComponentType<IconProps> | null> {
  const exportName = toPascalIconName(name)
  const modulePath = `../../node_modules/@phosphor-icons/react/dist/csr/${exportName}.es.js`
  const loadModule = PHOSPHOR_ICON_MODULES[modulePath]
  if (!loadModule) return null

  try {
    const module = await loadModule()
    return module[exportName] ?? module[`${exportName}Icon`] ?? null
  } catch {
    return null
  }
}
