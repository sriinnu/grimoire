public struct GrimoireGlyphView: View {
    private let glyph: GrimoireGlyph
    private let size: CGFloat
    private let color: Color

    public init(_ glyph: GrimoireGlyph, size: CGFloat = 20, color: Color = .primary) {
        self.glyph = glyph
        self.size = size
        self.color = color
    }

    public var body: some View {
        glyph.image(size: size, color: color)
    }
